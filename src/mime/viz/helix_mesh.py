"""Generate triangulated surface mesh for UMR helical microrobot visualization.

Produces vertices + triangle indices for the body cylinder, cone tip,
and helical fin blades. Matches the geometry in helix_geometry.py exactly.

All dimensions in the same units as the caller (typically metres).
"""

from __future__ import annotations

import math

import numpy as np


def generate_umr_mesh(
    body_radius: float,
    body_length: float,
    cone_length: float,
    cone_end_radius: float,
    fin_outer_radius: float,
    fin_length: float,
    fin_thickness: float,
    helix_pitch: float,
    n_fin_sets: int = 2,
    fins_per_set: int = 3,
    n_circ: int = 32,
    n_axial_body: int = 20,
    n_axial_cone: int = 10,
    n_axial_fin: int = 16,
    n_radial_fin: int = 6,
) -> tuple[np.ndarray, np.ndarray]:
    """Generate UMR surface mesh matching helix_geometry.py conventions.

    Returns
    -------
    vertices : (N, 3) float32
    triangles : (M, 3) int32
    """
    verts_list = []
    tris_list = []
    offset = 0

    def _add(v, t):
        nonlocal offset
        verts_list.append(v)
        tris_list.append(t + offset)
        offset += len(v)

    # Z axis is body axis. Body centered at origin.
    total_length = body_length + cone_length
    z_start = -total_length / 2.0
    z_body_end = z_start + body_length
    z_cone_end = z_start + total_length

    # Body cylinder
    _add(*_tube(body_radius, body_radius, z_start, z_body_end, n_circ, n_axial_body))
    # Cone tip
    _add(*_tube(body_radius, cone_end_radius, z_body_end, z_cone_end, n_circ, n_axial_cone))
    # End caps
    _add(*_disk(body_radius, z_start, n_circ))
    _add(*_disk(cone_end_radius, z_cone_end, n_circ))

    # Helical fins — match helix_geometry.py distribution exactly
    n_total_fins = n_fin_sets * fins_per_set
    # Same formula as create_discontinuous_fins_mask
    set_angular_offset = 2.0 * math.pi / n_fin_sets / fins_per_set
    z_body_start = -body_length / 2.0  # fins only along body, not cone

    for s in range(n_fin_sets):
        for f_idx in range(fins_per_set):
            fin_global = s * fins_per_set + f_idx
            z_fin_center = z_body_start + (fin_global + 0.5) * body_length / n_total_fins

            theta_base = (2.0 * math.pi * z_fin_center / helix_pitch
                          + f_idx * 2.0 * math.pi / fins_per_set
                          + s * set_angular_offset)

            _add(*_helical_blade(
                body_radius, fin_outer_radius, fin_length,
                helix_pitch, z_fin_center, theta_base,
                n_axial_fin, n_radial_fin,
            ))

    vertices = np.concatenate(verts_list, axis=0).astype(np.float32)
    triangles = np.concatenate(tris_list, axis=0).astype(np.int32)
    return vertices, triangles


def _tube(r_lo, r_hi, z_lo, z_hi, n_circ, n_axial):
    """Cylinder or cone surface (tube with varying radius)."""
    thetas = np.linspace(0, 2 * math.pi, n_circ, endpoint=False)
    zs = np.linspace(z_lo, z_hi, n_axial)
    verts = []
    for j, z in enumerate(zs):
        t = j / max(n_axial - 1, 1)
        r = r_lo * (1 - t) + r_hi * t
        for th in thetas:
            verts.append([r * math.cos(th), r * math.sin(th), z])
    verts = np.array(verts)
    tris = []
    for j in range(n_axial - 1):
        for i in range(n_circ):
            i1 = (i + 1) % n_circ
            a, b = j * n_circ + i, j * n_circ + i1
            c, d = (j + 1) * n_circ + i, (j + 1) * n_circ + i1
            tris.extend([[a, b, d], [a, d, c]])
    return verts, np.array(tris)


def _disk(radius, z, n_circ):
    """Flat disk end cap (triangle fan)."""
    thetas = np.linspace(0, 2 * math.pi, n_circ, endpoint=False)
    verts = [[0, 0, z]]
    for th in thetas:
        verts.append([radius * math.cos(th), radius * math.sin(th), z])
    tris = [[0, i + 1, (i % n_circ) + 2] for i in range(n_circ)]
    # Fix last triangle to wrap
    tris[-1][2] = 1
    return np.array(verts), np.array(tris)


def _helical_blade(
    body_radius, fin_outer_radius, fin_length,
    helix_pitch, z_center, theta_base,
    n_axial, n_radial,
):
    """Single helical fin blade with physical thickness.

    At each Z position, the blade's angular center follows:
        theta(z) = theta_base + 2*pi*(z - z_center)/helix_pitch
    matching create_discontinuous_fins_mask() in helix_geometry.py.

    The blade extends radially from body_radius to fin_outer_radius.
    Thickness is generated by offsetting two surfaces +/- half the
    angular width of the blade, then closing the outer and side edges.
    """
    # Angular half-width of the blade (from fin_thickness in the mask code)
    # fin_thickness is circumferential thickness at body_radius
    # angular_half = fin_thickness / (2 * body_radius)
    # Use a visible minimum so the blade is clearly 3D
    angular_half = max(0.15 / max(body_radius * 1000, 1e-3), 0.08)  # ~0.08 rad minimum

    z_lo = z_center - fin_length / 2
    z_hi = z_center + fin_length / 2
    zs = np.linspace(z_lo, z_hi, n_axial)
    # Start slightly inside body so fins visually merge with cylinder
    r_inner = body_radius * 0.85
    rs = np.linspace(r_inner, fin_outer_radius, n_radial)

    # Two surfaces: "front" (+angular_half) and "back" (-angular_half)
    n_ring = n_radial  # verts per axial row per side
    verts = []

    # Front face vertices
    for z in zs:
        theta_center = theta_base + 2.0 * math.pi * (z - z_center) / helix_pitch
        theta = theta_center + angular_half
        for r in rs:
            verts.append([r * math.cos(theta), r * math.sin(theta), z])

    # Back face vertices
    for z in zs:
        theta_center = theta_base + 2.0 * math.pi * (z - z_center) / helix_pitch
        theta = theta_center - angular_half
        for r in rs:
            verts.append([r * math.cos(theta), r * math.sin(theta), z])

    verts = np.array(verts)
    n_front = n_axial * n_ring  # vertex count per side

    tris = []

    # Front face triangles
    for j in range(n_axial - 1):
        for i in range(n_ring - 1):
            a = j * n_ring + i
            b = a + 1
            c = (j + 1) * n_ring + i
            d = c + 1
            tris.extend([[a, b, d], [a, d, c]])

    # Back face triangles (reversed winding)
    for j in range(n_axial - 1):
        for i in range(n_ring - 1):
            a = n_front + j * n_ring + i
            b = a + 1
            c = n_front + (j + 1) * n_ring + i
            d = c + 1
            tris.extend([[a, d, b], [a, c, d]])

    # Outer edge strip (connect front and back at r = fin_outer_radius)
    for j in range(n_axial - 1):
        f_outer = j * n_ring + (n_ring - 1)
        f_outer_next = (j + 1) * n_ring + (n_ring - 1)
        b_outer = n_front + j * n_ring + (n_ring - 1)
        b_outer_next = n_front + (j + 1) * n_ring + (n_ring - 1)
        tris.extend([
            [f_outer, f_outer_next, b_outer_next],
            [f_outer, b_outer_next, b_outer],
        ])

    return verts, np.array(tris)
